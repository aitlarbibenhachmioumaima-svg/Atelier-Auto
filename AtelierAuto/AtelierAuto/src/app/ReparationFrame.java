    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        try {
        Reparation r = new Reparation();

        r.setVehiculeId(Integer.parseInt(txtVehiculeId.getText()));
        java.util.Date utilDate = txtDate.getDate();
        LocalDate localDate = utilDate.toInstant()
        .atZone(java.time.ZoneId.systemDefault())
        .toLocalDate();

         r.setDate(localDate);
        r.setType(txtType.getText());
        r.setCout(Double.parseDouble(txtCout.getText()));

        service.create(r);
        loadReparations();

        txtVehiculeId.setText("");
           txtDate.setDate(null);
        txtType.setText("");
        txtCout.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
   }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
int row = jTable1.getSelectedRow();

    if (row >= 0) {
        int id = (int) jTable1.getValueAt(row, 0);

        try {
            service.supprimer(id);
            loadReparations();
        } catch (Exception e) {
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Sélectionnez une réparation");
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSupprimerActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
int row = jTable1.getSelectedRow();

    if (row >= 0) {
        try {
            Reparation r = new Reparation();

            r.setId((int) jTable1.getValueAt(row, 0));
            r.setVehiculeId(Integer.parseInt(txtVehiculeId.getText()));
            java.util.Date utilDate = txtDate.getDate();
            LocalDate localDate = utilDate.toInstant()
        .atZone(java.time.ZoneId.systemDefault())
        .toLocalDate();

r.setDate(localDate);
            r.setType(txtType.getText());
            r.setCout(Double.parseDouble(txtCout.getText()));

            service.modifier(r);
            loadReparations();

        } catch (Exception e) {
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Sélectionnez une réparation");
      }        // TODO add your handling code here:
    }//GEN-LAST:event_btnModifierActionPerformed
  
