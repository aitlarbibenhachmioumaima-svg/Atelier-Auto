    private void loadReparations() {
        try {
            List<Reparation> list = service.findAll();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            for (Reparation r : list) {
                model.addRow(new Object[]{
                        r.getId(),
                        r.getVehiculeId(),
                        r.getDate(),
                        r.getType(),
                        r.getCout()
                });
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

